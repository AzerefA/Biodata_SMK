    private void tabelDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelDataMouseClicked
        // TODO add your handling code here:
        int baris = tabelData.getSelectedRow();

        if (baris != -1) {
            NIS.setText(tabelData.getValueAt(baris, 0).toString());
            NAMA.setText(tabelData.getValueAt(baris, 1).toString());
            if ("Laki-Laki".equals(tabelData.getValueAt(baris, 2).toString())) {
                laki.setSelected(true);
            }else{
                Perem.setSelected(true);
            }
            Kelas.setText(tabelData.getValueAt(baris, 3).toString());
            Email.setText(tabelData.getValueAt(baris, 4).toString());
            Alamat.setText(tabelData.getValueAt(baris, 5).toString());
            tempatL.setText(tabelData.getValueAt(baris, 6).toString());
            SimpleDateFormat date = new SimpleDateFormat("yyyy-mm-dd");
            Date dateFormat = null;
            try {
            dateFormat = date.parse(tabelData.getValueAt(baris, 7).toString());
            } catch (ParseException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }
            tanggal.setDate(dateFormat);

            setTanggal();
            setJam();
        }
    }//GEN-LAST:event_tabelDataMouseClicked

